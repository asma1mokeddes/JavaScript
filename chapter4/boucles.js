// Boucle for :
for( let i=0; i<3; i++){
    if (i==1){
        continue;
    }
    console.log(i);
}
// break : interrompre l'exécution -> sortir d'une boucle
// continue : stopper l'exécution des instructions pour l'itération courante et l'exécution est reprise à l'itération suivante.



// Boucle while :
